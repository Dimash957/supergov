echo "======================================================================"
echo "🚀 eGov API Integration - Setup"
echo "======================================================================"
echo ""

# 1. Проверить что находимся в правильной папке
if [ ! -f "app/main.py" ]; then
    echo "❌ Пожалуйста, запустите из папки backend/"
    exit 1
fi

echo "✅ Вы в папке backend/"
echo ""

# 2. Проверить интеграцию
echo "🔍 Проверка интеграции..."
